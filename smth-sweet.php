          <h3>The Challenge</h3>
          <p>Based on my research, I came up with a design challenge:</p>
          <div class="project-question">
            <p>How can we help Asian dessert lovers to enjoy fresh desserts directly at home?</p>
        </div>
        </div>

        <div class="project-body">
          <h3>The Solution</h3>
          <p>Somethin' Sweet is an mobile platform for customers to buy Asian desserts making kits. It provides step-by-step guide for each dessert so everyone can make their favorite Asian dessert easily at home.</p>
          <p>It is hope that the app can help dessert lovers to overcome their dessert cravings, as well as build a online community to share and spread the love for Asian desserts. </p>
          <p>The app can also help connect people during the COVID-10 period and allows them to cook/eat what they love even at home.</p>
        </div>
        <div class="project-body">
          <h3>Market Research</h3>
          <p>During my research, I found some interesting data about Asian American demographics as well as Asian desserts trends.</p>
          <div class="project-photo large">
            <img src="images/smth-sweet/smth-sweet-research.PNG" alt="">
          </div>
          <p>One of the key insights that I found is that Asian American love to <strong>connect with others </strong>through cooking and they like their to food to be made with <strong>fresh ingredients</strong>.</p>
        </div>

        <div class="project-body">
          <h3>Competitor Analysis</h3>
          <p>Based on my research, I conducted a competitor analysis to understand what are the existing market is offering to their customers and what might be missing from them.</p>
          <div class="project-photo large">
            <img src="images/smth-sweet/smth-sweet-competitor.PNG" alt="">
            
          </div>
          <h3>User Interview</h3>
          <p>I conducted user interviews with 3 Asian dessert lovers who have been making Asian desserts at home. I want to discover their pain points and needs in making Asian desserts. Here are some key takeaways:</p>
          <li>Hard to get the Asian desserts ingredients easily in America.</li>
          <li>Authentic recipes are hard to find.</li>
          <br>
          <p>Based on my findings, I recognized the customers' needs for:</p>
          <li>A online store that sells Asian specific desserts with pre-measured kits so they can make desserts directly at home.</li>
          <li>A platform that allows people to share and connect with one another to exchange thoughts on desserts.</li>
        </div>
          
